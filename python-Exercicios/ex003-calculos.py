n1 = float(input('Digite um número: '))
n2 = float(input('digite mais um número: '))
soma = n1 + n2
sub = n1 - n2
div = n1 / n2
mult = n1 * n2


print('A soma entre {} e {} é {}'.format(n1, n2, soma))
print('A subtração entre {} e {} é {:.2f}'.format(n1, n2, sub))
print('A divisão entre {} e {} é {:.2f}'.format(n1, n2, div))
print('A multiploicação entre {} e {} é {}'.format(n1, n2, mult))

